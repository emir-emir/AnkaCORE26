public class MantiksalOperatorler {
    public static void main(String[] args) {

        boolean kosul1 = true;
        boolean kosul2 = false;

        System.out.println("kosul1 && kosul2 : " + (kosul1 && kosul2));
        System.out.println("kosul1 || kosul2 : " + (kosul1 || kosul2));
        System.out.println("!kosul1 : " + (!kosul1));
        System.out.println("!kosul2 : " + (!kosul2));
    }
}
