package Metodlar.ornek;


public class DortIslem {

    // metodlar
    int topla(int a, int b) {

        return a + b;
    }

    int cikar(int a, int b) {
        return a - b;
    }

    int carp(int a, int b) {
        return a * b;
    }

    double bol(int a, int b) {
        return (double) a / b;
    }

}
